class STGCN(nn.Module):
    def __init__(
        self,
        in_feats: int,
        gcn_hidden: int = 64,
        tcn_hidden: int = 64,
        out_dim: int = 1,
        kernel_size: int = 3,
        dropout: float = 0.2,
        mode: str = "regression",
    ):
        super().__init__()
        self.mode = mode

        # Spatial graph convolution
        self.gcn1 = GCNConv(in_feats, gcn_hidden)
        self.gcn2 = GCNConv(gcn_hidden, gcn_hidden)

        # Temporal convolution (ST-GCN key difference)
        self.tcn = nn.Sequential(
            nn.Conv2d(
                in_channels=gcn_hidden,
                out_channels=tcn_hidden,
                kernel_size=(kernel_size, 1),
                padding=(kernel_size // 2, 0),
            ),
            nn.ReLU(),
            nn.Dropout(dropout),
        )

        self.mlp = nn.Sequential(
            nn.Linear(tcn_hidden, tcn_hidden // 2),
            nn.ReLU(),
            nn.Linear(tcn_hidden // 2, out_dim),
        )

    def forward(
        self,
        seq_feats: torch.Tensor,
        seq_edge_index: List[List[np.ndarray]],
        seq_edge_weight: List[List[np.ndarray]],
    ):
        """
        seq_feats: (batch, seq_len, N, F)
        """
        batch, seq_len, N, F_dim = seq_feats.shape
        device = seq_feats.device

        gcn_outputs = []

        # ----- Spatial graph convolution -----
        for t in range(seq_len):
            batch_node_embeds = []
            for b in range(batch):
                x = seq_feats[b, t]

                ei_np = seq_edge_index[b][t]
                ew_np = seq_edge_weight[b][t]

                if ei_np.size == 0:
                    edge_index = torch.empty((2, 0), dtype=torch.long, device=device)
                    edge_weight = None
                else:
                    edge_index = torch.from_numpy(ei_np).to(device)
                    edge_weight = torch.from_numpy(ew_np).to(device)

                h = F.relu(self.gcn1(x, edge_index, edge_weight))
                h = F.relu(self.gcn2(h, edge_index, edge_weight))
                batch_node_embeds.append(h)

            gcn_outputs.append(torch.stack(batch_node_embeds, dim=0))

        # (batch, seq_len, N, gcn_hidden)
        x = torch.stack(gcn_outputs, dim=1)

        # ----- Temporal convolution -----
        # reshape to (batch, channels, time, nodes)
        x = x.permute(0, 3, 1, 2)
        x = self.tcn(x)

        # take last time step
        x = x[:, :, -1, :]  # (batch, tcn_hidden, N)
        x = x.permute(0, 2, 1)  # (batch, N, tcn_hidden)

        preds = self.mlp(x)

        if self.mode == "classification":
            preds = torch.sigmoid(preds)

        return preds.squeeze(-1) if preds.shape[-1] == 1 else preds



